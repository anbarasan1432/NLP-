import nltk
from nltk.tokenize import word_tokenize
from nltk.util import ngrams
from nltk.probability import FreqDist

nltk.download('punkt') # Reverting to standard 'punkt' resource

# Sample text
text = "Language modeling is an important part of natural language processing"

# Tokenize words
tokens = word_tokenize(text.lower())

# Create bigrams
bigrams = list(ngrams(tokens, 2))

# Frequency distribution
freq = FreqDist(bigrams)

print("Bigram Frequencies:")
for k, v in freq.items():
    print(k, ":", v)

print("\nMost Common Bigrams:")
print(freq.most_common(5))

import nltk
nltk.download('punkt')

from nltk.tokenize import word_tokenize
from nltk.util import ngrams
from nltk.probability import FreqDist

text = "Natural language processing enables machines to understand human language"

# Tokenization
tokens = word_tokenize(text.lower())

# Unigrams, Bigrams, Trigrams
unigrams = tokens
bigrams = list(ngrams(tokens, 2))
trigrams = list(ngrams(tokens, 3))

print("\nUnigrams:")
print(unigrams)

print("\nBigrams:")
print(bigrams)

print("\nTrigrams:")
print(trigrams)

# Frequency distribution for trigrams
freq_trigrams = FreqDist(trigrams)
print("\nTrigram Frequencies:")
for k, v in freq_trigrams.items():
    print(k, ":", v)

print("\nMost Common Trigrams:")
print(freq_trigrams.most_common(5))